#include <iostream>
using namespace std;
int a[400][400],num,b[400][400],c[5000][2],cc[5000][2],l1,l2;

int main()
{
	int x,y,p,n,xx,i,j,x1,x2,y1,y2;
	memset(a,0,sizeof(a));
	a[200][200]=1;
	a[202][200]=2;
	num=2;
	p=1;
	x=202;
	y=200;
	n=xx=0;
	while(num<=10000)
	{
		if(xx==p||(xx==p-1&&n==0))
		{
			n=(n+1)%6;
			xx=0;
			if(n==5)
				p++;
		}
		num++;
		if(n==0)
		{
			a[x+1][y-1]=num;
			x++;
			y--;
			xx++;
		}
		else if(n==1)
		{
			a[x-1][y-1]=num;
			x--;
			y--;
			xx++;
		}
		else if(n==2)
		{
			a[x-2][y]=num;
			x-=2;
			xx++;
		}
		else if(n==3)
		{
			a[x-1][y+1]=num;
			x--;
			y++;
			xx++;
		}
		else if(n==4)
		{
			a[x+1][y+1]=num;
			x++;
			y++;
			xx++;
		}
		else if(n==5)
		{
			a[x+2][y]=num;
			x+=2;
			xx++;
		}
	}
	while(scanf("%d%d",&x,&y)&&(x||y))
	{
		for(i=0;i<400;i++)
		{
			for(j=0;j<400;j++)
				if(x==a[i][j])
				{
					x1=i;
					y1=j;
					break;
				}
			if(j<400)
				break;
		}
		for(i=0;i<400;i++)
		{
			for(j=0;j<400;j++)
				if(y==a[i][j])
				{
					x2=i;
					y2=j;
					break;
				}
			if(j<400)
				break;
		}
		for(i=0;i<400;i++)
			for(j=0;j<400;j++)
				b[i][j]=999999;
		memset(c,0,sizeof(c));
		b[x1][y1]=0;
		l1=1;
		c[0][0]=x1;
		c[0][1]=y1;
		while(b[x2][y2]==999999)
		{
			memset(cc,0,sizeof(cc));
			l2=0;
			for(i=0;i<l1;i++)
			{
				if(!a[c[i][0]][c[i][1]])
					continue;
				if(b[c[i][0]+2][c[i][1]]>b[c[i][0]][c[i][1]]+1)
				{
					b[c[i][0]+2][c[i][1]]=b[c[i][0]][c[i][1]]+1;
					cc[l2][0]=c[i][0]+2;
					cc[l2][1]=c[i][1];
					l2++;
				}
				if(b[c[i][0]-1][c[i][1]+1]>b[c[i][0]][c[i][1]]+1)
				{
					b[c[i][0]-1][c[i][1]+1]=b[c[i][0]][c[i][1]]+1;
					cc[l2][0]=c[i][0]-1;
					cc[l2][1]=c[i][1]+1;
					l2++;
				}
				if(b[c[i][0]+1][c[i][1]+1]>b[c[i][0]][c[i][1]]+1)
				{
					b[c[i][0]+1][c[i][1]+1]=b[c[i][0]][c[i][1]]+1;
					cc[l2][0]=c[i][0]+1;
					cc[l2][1]=c[i][1]+1;
					l2++;
				}
				if(b[c[i][0]-2][c[i][1]]>b[c[i][0]][c[i][1]]+1)
				{
					b[c[i][0]-2][c[i][1]]=b[c[i][0]][c[i][1]]+1;
					cc[l2][0]=c[i][0]-2;
					cc[l2][1]=c[i][1];
					l2++;
				}
				if(b[c[i][0]-1][c[i][1]-1]>b[c[i][0]][c[i][1]]+1)
				{
					b[c[i][0]-1][c[i][1]-1]=b[c[i][0]][c[i][1]]+1;
					cc[l2][0]=c[i][0]-1;
					cc[l2][1]=c[i][1]-1;
					l2++;
				}
				if(b[c[i][0]+1][c[i][1]-1]>b[c[i][0]][c[i][1]]+1)
				{
					b[c[i][0]+1][c[i][1]-1]=b[c[i][0]][c[i][1]]+1;
					cc[l2][0]=c[i][0]+1;
					cc[l2][1]=c[i][1]-1;
					l2++;
				}
			}
			l1=l2;
			memcpy(c,cc,sizeof(cc));
		}
		printf("%d\n",b[x2][y2]);
	}
	return 0;
}

